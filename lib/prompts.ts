export function getPrompt(meetingType: MeetingType): string {
  switch (meetingType) {
    case 'checkup': return CHECKUP_PROMPT;
    case 'investment': return INVESTMENT_PROMPT;
    case 'intro': return INTRO_PROMPT;
    case 'process': return PROCESS_PROMPT;
  }
}
